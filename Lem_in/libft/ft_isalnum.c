#include "libft.h"

int		ft_isalnum(int ch)
{
	if ((('a' <= ch && ch <= 'z') || ('A' <= ch && ch <= 'Z')) ||
		(ch <= '9' && ch >= '0'))
		return (1);
	else
		return (0);
}
